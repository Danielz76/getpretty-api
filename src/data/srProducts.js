function filterForHomeUse(products) {
  return products.filter(p => !p.professional_only);
}

function filterForPregnancy(products) {
  return products.filter(p => p.pregnancy_safe);
}

function filterForDarkSkin(products, fitzpatrick) {
  if (fitzpatrick >= 4) {
    return products.filter(p => p.dark_skin_safe);
  }
  return products;
}

